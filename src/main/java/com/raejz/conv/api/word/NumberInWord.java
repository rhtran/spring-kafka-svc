package com.raejz.conv.api.word;

public class NumberInWord {
    private String numString;
    private String status;
    private String numInEnglish;

    public String getNumString() {
        return numString;
    }

    public void setNumString(String numString) {
        this.numString = numString;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getNumInEnglish() {
        return numInEnglish;
    }

    public void setNumInEnglish(String numInEnglish) {
        this.numInEnglish = numInEnglish;
    }
}
